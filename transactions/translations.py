def get_translation(key, language='es'):
    """Función simple para obtener traducción."""
    # Asegurar que el idioma sea válido
    if language not in TRANSLATIONS:
        language = 'es'
    
    # Retornar la traducción o la clave si no existe
    return TRANSLATIONS[language].get(key, key)


def get_current_translations(language='es'):
    """Obtiene todas las traducciones para un idioma."""
    return TRANSLATIONS.get(language, TRANSLATIONS['es'])

